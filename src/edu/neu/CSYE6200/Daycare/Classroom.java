package edu.neu.CSYE6200.Daycare;

public class Classroom {
	public int ID;
	public String classroom_name;
	public int grade;
	public int classroom_number;

	public Classroom(int ID, String classroom_name, int grade, int classroom_number) {
		this.ID = ID;
		this.classroom_name = classroom_name;
		this.grade = grade;
		this.classroom_number = classroom_number;
	}
}
